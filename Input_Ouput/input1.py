b = int(input("Enter The Value : "))
print(b)                # 5
print(type(b))         # <class 'int'>


c = float(input("Enter The Value : "))
print(c)                # 5.0
print(type(c))         # <class 'float'>


d = bool(input("Enter The Value : "))
print(d)                # True
print(type(d))         # <class 'bool'>
